// Match the longest known opening for a given SAN move sequence
function identifyOpening(sanMoves) {
    if (!sanMoves || !sanMoves.length) return null;
    const key = sanMoves.join(' ');
    let best = null;
    for (const [moves, name, eco] of OPENINGS_DB) {
        if (key.startsWith(moves) || key === moves) {
            if (!best || moves.length > best.moves.length) {
                best = { moves, name, eco };
            }
        }
    }
    return best;
}
